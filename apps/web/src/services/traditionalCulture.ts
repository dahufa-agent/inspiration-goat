// 分析并标记低质量知识元素
export function getLowQualityElements(): Array<{
  elementId: string
  elementType: KnowledgeElementType
  reason: string
}> {
  // 动态分析各知识库元素的使用效果
  const lowQuality: Array<{
    elementId: string
    elementType: KnowledgeElementType
    reason: string
  }> = []
  
  // 检查36计
  Object.entries(TRADITIONAL_CULTURE.strategies36).forEach(([name, info]) => {
    // 基于关键词覆盖度判断是否有改进空间
    if (info.keywords.length < 2) {
      lowQuality.push({
        elementId: name,
        elementType: 'strategy',
        reason: '关键词覆盖度较低，可能影响匹配准确性',
      })
    }
  })
  
  return lowQuality
}

// 建议新增的国学元素
export function suggestNewElements(): string[] {
  const suggestions: string[] = []
  
  // 基于当前知识库空白分析
  // 例如：检查是否有其他季节/节日的诗词意象
  
  const existingSeasons = Object.keys(TRADITIONAL_CULTURE.poetryImages)
  
  if (!existingSeasons.includes('黄昏')) {
    suggestions.push('黄昏意象（如：夕阳西下、断肠人在天涯、落日余晖）')
  }
  
  if (!existingSeasons.includes('黎明')) {
    suggestions.push('黎明意象（如：日出东方、晨曦微露、破晓）')
  }
  
  if (!existingSeasons.includes('夜晚')) {
    suggestions.push('夜晚意象（如：月明星稀、灯火阑珊、夜色朦胧）')
  }
  
  return suggestions
}
