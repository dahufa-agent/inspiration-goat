// 分析用户输入，自动关联国学元素
export function analyzeTraditionalCulture(input: string): {
  matchedStrategies: string[];
  matchedImages: string[];
  matchedFigures: string[];
  matchedIdioms: string[];
  visualStyle: string;
} {
  const lowerInput = input.toLowerCase()
  const matchedStrategies: string[] = []
  const matchedImages: string[] = []
  const matchedFigures: string[] = []
  const matchedIdioms: string[] = []

  // 匹配36计
  for (const [name, info] of Object.entries(TRADITIONAL_CULTURE.strategies36)) {
    if (info.keywords.some(k => lowerInput.includes(k)) || lowerInput.includes(name)) {
      matchedStrategies.push(name)
    }
  }

  // 匹配诗词意象
  for (const [name, info] of Object.entries(TRADITIONAL_CULTURE.poetryImages)) {
    if (lowerInput.includes(name) || info.images.some(img => lowerInput.includes(img)) || info.poems.some(p => lowerInput.includes(p))) {
      matchedImages.push(name)
    }
  }

  // 匹配历史人物
  for (const [name, info] of Object.entries(TRADITIONAL_CULTURE.historicalFigures)) {
    if (lowerInput.includes(name) || info.traits.some(t => lowerInput.includes(t)) || info.stories.some(s => lowerInput.includes(s))) {
      matchedFigures.push(name)
    }
  }

  // 匹配成语典故
  for (const [name, info] of Object.entries(TRADITIONAL_CULTURE.idioms)) {
    if (lowerInput.includes(name) || lowerInput.includes(info.story.slice(0, 10))) {
      matchedIdioms.push(name)
    }
  }

  // 自动选择视觉风格
  let visualStyle = '水墨'
  if (lowerInput.includes('敦煌') || lowerInput.includes('飞天') || lowerInput.includes('佛教')) visualStyle = '敦煌'
  else if (lowerInput.includes('山水') || lowerInput.includes('自然')) visualStyle = '青绿山水'
  else if (lowerInput.includes('人物') || lowerInput.includes('美女') || lowerInput.includes('古风')) visualStyle = '工笔人物'
  else if (lowerInput.includes('红色') || lowerInput.includes('剪纸') || lowerInput.includes('传统')) visualStyle = '剪纸'
  else if (lowerInput.includes('刺绣') || lowerInput.includes('锦')) visualStyle = '刺绣'

  return {
    matchedStrategies,
    matchedImages,
    matchedFigures,
    matchedIdioms,
    visualStyle
  }
}

// 生成故事脚本（融合国学元素）
export function generateStoryWithCulture(prompt: string): {
  story: string;
  storyboard: string[];
  culturalElements: string[];
} {
  const analysis = analyzeTraditionalCulture(prompt)
  const elements: string[] = []

  if (analysis.matchedStrategies.length > 0) {
    const strategy = analysis.matchedStrategies[0]
    const info = TRADITIONAL_CULTURE.strategies36[strategy as keyof typeof TRADITIONAL_CULTURE.strategies36]
    elements.push(`【三十六计·${strategy}】${info.story}`)
  }

  if (analysis.matchedFigures.length > 0) {
    const figure = analysis.matchedFigures[0]
    const info = TRADITIONAL_CULTURE.historicalFigures[figure as keyof typeof TRADITIONAL_CULTURE.historicalFigures]
    elements.push(`【历史人物·${figure}】${info.era}，${info.traits.join('、')}`)
  }

  if (analysis.matchedIdioms.length > 0) {
    const idiom = analysis.matchedIdioms[0]
    const info = TRADITIONAL_CULTURE.idioms[idiom as keyof typeof TRADITIONAL_CULTURE.idioms]
    elements.push(`【成语典故·${idiom}】${info.story}`)
  }

  if (analysis.matchedImages.length > 0) {
    const image = analysis.matchedImages[0]
    const info = TRADITIONAL_CULTURE.poetryImages[image as keyof typeof TRADITIONAL_CULTURE.poetryImages]
    elements.push(`【诗词意境·${image}】${info.images.join('、')}，${info.poems.join('、')}`)
  }

  // 生成故事脚本
  const story = `【创作灵感】
${prompt}

【融入国学元素】
${elements.length > 0 ? elements.join('\n') : '以现代视角诠释传统文化之美'}

【故事结构】
- 开篇：以意象场景入题
- 发展：融入传统文化智慧
- 高潮：冲突与转折
- 结尾：意境留白，余韵悠长

【核心主题】
传统智慧与现代生活的融合，呈现东方美学的独特魅力`

  // 生成分镜描述
  const style = TRADITIONAL_CULTURE.visualStyles[analysis.visualStyle as keyof typeof TRADITIONAL_CULTURE.visualStyles]
  const storyboard = [
    `镜头1：全景 ${analysis.matchedImages[0] || '山水云雾'} 场景，大远景，${style.prompts[0]}`,
    `镜头2：中景 古风人物登场，${style.prompts[1]}，慢镜头推进`,
    `镜头3：特写 关键道具或表情，${style.prompts[2]}，光影变化`,
    `镜头4：全景 场景转换，${style.prompts[3]}，运镜环绕`,
    `镜头5：特写 结局意境留白，${style.prompts[0]}，淡出`
  ]

  return {
    story,
    storyboard,
    culturalElements: elements
  }
}

