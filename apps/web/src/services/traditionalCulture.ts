import { recordKnowledgeUsage, collectEvolutionCandidate, generateKnowledgeProposal } from './preferenceStore'

// 知识元素类型
type KnowledgeElementType = 'strategy' | 'idiom' | 'figure' | 'image' | 'visual_style'

// 记录国学元素使用并跟踪质量
export function trackCultureElementUsage(
  elementId: string,
  elementType: KnowledgeElementType,
  feedback?: 'positive' | 'negative',
  qualityScore?: number
): void {
  // 调用偏好存储记录使用
  recordKnowledgeUsage(elementId, elementType, feedback, qualityScore)
  
  // 如果质量评分低于阈值，自动收集进化候选
  if (qualityScore !== undefined && qualityScore < 6) {
    collectEvolutionCandidate(
      'knowledge_feedback',
      `知识元素 "${elementId}" 评分较低 (${qualityScore}/10)`,
      'knowledge_quality_tracking',
      {
        reason: `用户对 "${elementId}" 的反馈评分${qualityScore}低于阈值6分`,
        suggestedAction: '建议优化或标记该知识元素待审核',
        confidence: 0.8,
        qualityScore,
      }
    )
  }
}

